package minggu5;

public class Mahasiswa {
    String nama;
    String nim;
    int tahunMasuk;
    int nilaiUTS;
    int nilaiUAS;

    Mahasiswa(String nm, String ni, int tm, int uts, int uas) {
        nama = nm;
        nim = ni;
        tahunMasuk = tm;
        nilaiUTS = uts;
        nilaiUAS = uas;
    }
}